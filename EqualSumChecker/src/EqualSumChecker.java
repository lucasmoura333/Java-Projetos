public class EqualSumChecker {

    public static boolean hasEqualSum(int parm1, int parm2, int parm3){
        int sumOfParms = (parm1 + parm2);

        if(sumOfParms == parm3){
            return true;
        }else{
            return false;
        }
    }
}


